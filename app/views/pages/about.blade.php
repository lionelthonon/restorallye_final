        <article>
            <h2>Le concept</h2>
            <p>
                L’asbl RestoRallye, animée par une équipe de quelques bénévoles, propose un concept ancré dans le monde gastronomique et profondément animé par l’adage du <span>« Dis-moi ce que tu manges je te dirai d’où tu viens »</span>.
            </p>
        </article>

        <article>
            <h2>Gastronomie</h2>
            <h3>Un itinéraire gourmand en trois restaurants</h3>
            <p>
                Pourquoi toujours se limiter à un seul restaurant pour déguster l’entièreté d’un menu ? RestoRallye offre la possibilité à ses « gastronomades » de faire la connaissance en une seule soirée de plusieurs restaurants d’une localité. Pour les participants, c’est aussi l’occasion de découvrir une nouvelle région de manière originale et ludique.
            </p>
            <p>
                A l’instar d’un rallye automobile, les convives se réunissent dans un espace, le plus souvent prestigieux et mis à disposition par les autorités communales, pour y déguster l’apéritif et les mises en bouche. Chacun y reçoit le « roadbook ». On y trouve un plan de la ville avec l’emplacement des restaurants participants, une série de bons gourmands donnant droit à un mets préparé par un des restaurateurs participants et l’itinéraire via trois restaurants, mis au point par notre équipe.
            </p>

            <p>
                Ensuite, c’est le départ vers l’inconnu, pas le choix, il faut expérimenter par les papilles ! C’est la seule constante des étapes… La première halte est consacrée à la dégustation de l’entrée froide, la deuxième à l’entrée chaude et enfin la troisième au plat principal. Tous les « RestoRallyens » se retrouveront enfin pour savourer le dessert et le café dans un même endroit où ils pourront échanger leur avis sur leur parcours respectif.
            </p>
        </article>

        <article>
            <h2>Culture</h2>
            <h3>La gastronomie comme fenêtre sur une région</h3>
            <p>
                Le RestoRallye s’attache à mettre en avant le patrimoine culturel d’une région ou d’une ville par le biais de sa cuisine. L’intérêt de ce concept est de faire venir dans une même localité, un large public, qui pourra en une soirée, se faire une idée concrète des richesses touristiques et gastronomiques de l’endroit. L’expérience nous a montré que si un RestoRallye attire les habitants des alentours, il attire également un nombre appréciable de personnes extraterritoriales curieuses de nouvelles sensations gustatives. En somme, le RestoRallye devient un prétexte pour découvrir ou redécouvrir le savoir-faire culinaire propre à chaque région.
            </p>
            <h3>Le RestoRallye : un faire-valoir original</h3>
            <p>
                Le RestoRallye est un événement d’importance permettant à toute une série de partenaires de promotionner leur travail et leurs produits le temps d’une soirée. De plus, il se déroule généralement un jeudi soir pour permettre aux restaurateurs participants de remplir leur établissement un jour dit ‘creux’. De cette façon, l’événement crée une animation gastronomique et touristique semblable à un soir de week-end dans la ville d’accueil.
            </p>
            <p>
                Pour les restaurateurs, c’est l’occasion de mettre en évidence une carte de visite gustative. Le RestoRallye est un outil promotionnel inédit et important pour les restaurateurs participants.
            </p>
            <p>
                Chaque restaurateur reçoit le même budget pour créer son menu à savoir : une entrée froide, une entrée chaude et un plat principal. En somme, il accueillera trois vagues successives de RestoRallyens (40 places dans le restaurant = 120 personnes qui découvrent sa cuisine).
            </p>
            <p>
                Dans l’idéal, le restaurateur doit, sur une seule préparation culinaire, séduire le RestoRallyen pour lui donner simplement l’envie de revenir dans son établissement un autre jour. Bref, c’est un moyen de promotion unique où le restaurateur s’adresse directement à un client potentiel par le biais de son savoir-faire.
            </p>
            <p>
                Toutefois, afin d’assurer un certain niveau qualitatif, tant pour les RestoRallyens que pour les restaurateurs, le nombre de couverts est limité par la capacité d’accueil totale des établissements participants.
            </p>
            <p>
                Dans ce même souci de qualité, c’est l’équipe de RestoRallye qui sélectionne les restaurants participants. Ceux-ci auront l’occasion de proposer des plats originaux et de haut niveau.
            </p>
        </article>

        <article>
            <h2>Découverte</h2>
            <h3>Un attrait ludique et touristique</h3>
            <p>
                Le RestoRallye permet à la ville d’accueil d’augmenter son attrait touristique le temps d’une soirée. En effet, les RestoRallyes se déroulent le plus souvent à pied et permettent une balade touristique dans le centre ville. Ce genre d’événement possède également la faculté de se fondre dans d’autres types activités initiées par la ville d’accueil. Pensons, par exemple, à la Ville de Durbuy où le RestoRallye a participé au festival de l’écrevisse fédérant plus de monde d’une manière ludique autour de cette manifestation orchestrée par le Syndicat d’Initiative. Les associations et les partenariats sont infinis car la formule attractive et inédite de RestoRallye possède la particularité de se prêter à tous les genres de manifestations globales en y apportant une convivialité gourmande. De plus en plus, les responsables communaux font appel à nous en direct afin de créer un événement dans leur circonscription.
            </p>
        </article>

